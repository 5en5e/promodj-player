function updateTrack(){
  updateProgressBar(formatCurrentTime(audio.currentTime), audio.currentTime/audio.duration);
  let playingTrack=getTrackByTime(audio.currentTime);
  if (currentTrack!=playingTrack){
    if (document.querySelector(".misc .loop").getAttribute("state")=="on"){
      playCurrentTrack();
    } else {
      currentTrack=playingTrack;
      undecorateOldTrack();
      decorateCurrentTrack();
    }
  }
}

function updateProgressBar(time,value){
  document.querySelector(".audio-progress .progress-value").textContent=" "+time;
  document.querySelector(".audio-progress .progress-value").style.width=`${value*100}%`;
}

function getTrackByTime(time){
  let tracks=document.querySelectorAll(".track");
  for (let x of tracks){
    let start = startTimeInSeconds(x);
    let next = x.nextElementSibling;
    let end = next ? startTimeInSeconds(next) : audio.duration;
    if ((time>=start && time<end)){
      return x;
    } else if (end==audio.duration){
        undecorateOldTrack();
        currentState = "none";
        toggleIcons("play");
        return x;
    }
  }
}

function formatCurrentTime(currentTime){
  const hours = parseInt(currentTime / 3600);
  const hoursStr = hours<10?"0"+hours:`${hours}`;
  const minutes = parseInt((currentTime / 60)%60);
  const minutesStr = minutes<10?"0"+minutes:`${minutes}`;
  const seconds = parseInt(currentTime % 60);
  const secondsStr = seconds<10?"0"+seconds:`${seconds}`;
  return `${hoursStr}:${minutesStr}:${secondsStr}`;
}

function populateAlbumList(){
  const parent = document.getElementsByClassName('left-container')[0];
  for (let item of allAlbums){
    const title = item.title;

    const album = document.createElement('div');
    album.classList.add('album');
    const id = item.id;
    album.setAttribute("atitle",`${id}`);

    const cover = document.createElement('div');
    cover.classList.add('cover');

    const name = document.createElement('div');
    name.classList.add('name');
    name.textContent=`${title}`;

    album.appendChild(cover);
    album.appendChild(name);
    parent.appendChild(album);
  }
}

function setCurrentAlbum(id){
  currentTrack = undefined;
  currentState = "none";
  toggleIcons("play");

  updateProgressBar("00:00:00", 0);

  currentAlbum = allAlbums.find(item => item.id === id);
  
  audio.pause();
  audio.src = currentAlbum.file;
  audio.load();

  const title = currentAlbum.title;

  const album = document.querySelector('.right-container .album');
  const name = document.querySelector('.right-container .album .name');
  album.setAttribute("atitle",`${id}`);
  name.textContent=`${title}`;
  
  const tracklist = document.getElementsByClassName('tracklist')[0];
  tracklist.innerHTML = '';
  for (let trackInfo of currentAlbum.tracks){
    const track = document.createElement('li');
    track.classList.add('track');
    track.setAttribute("starttime",`${trackInfo.starttime}`);
    track.setAttribute('active', "false");
    currentTrack ??= track;

    const pp = document.createElement('span');
    pp.classList.add('play-pause');
    

    const trackTitle = document.createElement('span');
    const trackPerformer = document.createElement('span');
    trackTitle.classList.add('track-title');
    trackPerformer.classList.add('track-performer');
    trackTitle.textContent = `${trackInfo.title}`
    trackPerformer.textContent = ` - ${trackInfo.performer}`
    
    const duration = document.createElement('span');
    duration.classList.add('duration');

    track.appendChild(pp);
    track.appendChild(trackTitle);
    track.appendChild(trackPerformer);
    track.appendChild(duration);    
    
    tracklist.appendChild(track);
  }
}

function playOrPause(){
  if (currentState == "none"){
      currentState = "playing";
      toggleIcons("pause");
      currentTrack = document.querySelector('.track:first-child');
      playCurrentTrack();
  } else if (currentState=="paused"){
      currentState = "playing";
      toggleIcons("pause");
      audio.play().catch((e)=>{console.error(e)});;
  } else if (currentState=="playing"){
      currentState = "paused";
      toggleIcons("play");
      audio.pause();
  } else {
    throw "currentState==???";
  }
}

function toggleIcons(icon){
  if (icon=="play"){
    currentTrack?.querySelector(".play-pause").setAttribute("icon", "play");
    document.querySelector(".order .play-pause").setAttribute("icon", "play");
    document.querySelector(".wrapper .play-pause").setAttribute("icon", "play");
  } else if (icon=="pause"){
    currentTrack?.querySelector(".play-pause").setAttribute("icon", "pause");
    document.querySelector(".order .play-pause").setAttribute("icon", "pause");
    document.querySelector(".wrapper .play-pause").setAttribute("icon", "pause");
  } else {
    throw "icon==???";
  }
}

function playCurrentTrack(){
  audio.pause();
  undecorateOldTrack();
  decorateCurrentTrack();
  const seconds = startTimeInSeconds(currentTrack);
  audio.currentTime=seconds;
  currentState = "playing";
  audio.play().catch((e)=>{console.error(e)});;
}

function undecorateOldTrack(){
  let oldTrack = document.querySelector('.track[active="true"]');
  oldTrack?.querySelector(".play-pause").setAttribute("icon", "play");
  oldTrack?.setAttribute("active","false");
}

function decorateCurrentTrack(){
  toggleIcons("pause");
  currentTrack.setAttribute("active","true");
}

function startTimeInSeconds(track){
  const str = track.getAttribute("starttime");
  const parts = str.split(":");
  const minutes = parseInt(parts[0], 10);
  const seconds = parseInt(parts[1], 10);
  return minutes * 60 + seconds;
}

