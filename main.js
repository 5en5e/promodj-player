document.querySelector(".controls .sound .mute").addEventListener("click", (e) => {
  const vol = document.querySelector('.controls .sound .volume [type="range"]');
  if (e.target.getAttribute("muted")=="false"){
    audio.muted=true; 
    controls.volume=vol.value;
    vol.value=0;
    e.target.setAttribute("muted", "true");   
  }
  else{
    e.target.setAttribute("muted", "false"); 
    vol.value=controls.volume;
    audio.muted=false;
  }
  e.stopPropagation();
});

